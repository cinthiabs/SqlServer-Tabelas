GENERO INT,
IDADELIM INT,
CONSTRAINT PK_CODGAME PRIMARY KEY(CODGAME),
CONSTRAINT FK_GENERO FOREIGN KEY(GENERO)
REFERENCES GENERO(CODGEN));

SELECT * FROM GAMES


